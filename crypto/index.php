  <h2>Major Concepts</h2>
  <p></p>
  <h3>Block-chain</h3>
  <p>​	A block-chain is like a rea chain. It goes link by link, each link (block) is the same size, and blocks only get added to the end of the chain. Bitcoin transactions only occur when a block, containing your transaction information, is processed and added to the chain. Once the official chain has your transaction posted your transaction has been processed. </p>
  <p></p>
  <h3>Cryptocurrency</h3>
  <p>​	A digital currency in which encryption techniques are used to regulate the generation of units of currency and verify the transfer of funds, operating independently of a central bank: </p>
  <p></p>
  <h3>Proof-of-work vs. Proof-of-stake</h3>
  <p>​	Proof of work and proof of stake are two different methods for creating blocks on a chain.</p>
